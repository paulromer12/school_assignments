// Java Program to Iterate through an array
// Using for-each loop

class ForEach {
    public static void main(String[] args) {      
    // Array declaration
    int arr[] = { 1, 2, 3, 4, 5 };
    
    // Using for-each loop to 
    // print each element
    for (int e : arr) {
        System.out.print(e + " ");
        }
    }
}